package com.ymagis.api;

public class ResponseVO {

  private String name;
  private String size;
  private String idQuiz;
  private String wPlace;
  private String gPlace;
  
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public String getSize() {
    return size;
  }
  public void setSize(String size) {
    this.size = size;
  }
  public String getIdQuiz() {
    return idQuiz;
  }
  public void setIdQuiz(String idQuiz) {
    this.idQuiz = idQuiz;
  }
  public String getwPlace() {
    return wPlace;
  }
  public void setwPlace(String wPlace) {
    this.wPlace = wPlace;
  }
  public String getgPlace() {
    return gPlace;
  }
  public void setgPlace(String gPlace) {
    this.gPlace = gPlace;
  }
  
}
